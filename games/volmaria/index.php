          timed interval based on the "Item in Hand" at the time the boost is to be applied. Timed Effects are applied every 5 seconds with a duration of 6 seconds (so if the same item is 
          continuously held the effect will stay constant)<br /><br />
          <ul>
          <li><em>Blaze Rod</em> - Strength I</li>
          <li><em>Ghast Tear</em> - Regeneration II</li>
          <li><em>Magma Cream</em> - Fire Resistance I</li>
          <li><em>Sugar</em> - Speed II</li>
          </ul><br />

          The Rogue Class also has some Instant Effect abilities. These Instant abilities are applied when the "Item In Hand" is "Right Clicked" however using an Instant Effect ability 
          creates a 30 second cool-down during witch time no other effect (Timed or Instant) can be used. Instant Effects: Are applied instantly when the "Item In Hand" is "Right Clicked"<br /><br />
          <ul>
          <li><em>Glistering Melon</em> - Heals 6 Hearts Instantly</li>
          <li><em>Wheat</em> - Heals 6 Hunger Instantly</li>
          <li><em>Nether Star</em> - 8 seconds of complete invisibility</li>
          </ul><br />

          <b>Advantages:</b>
          <ul>
            <li>Arrows can do significant damage when multiplied by 2x or greater. By adding other enchantments you can do even more damage!</li>
            <li>Do the most damage from long range attacks</li>
          </ul>
          <b>Disadvantages:</b>
          <ul>
            <li>Very low protection! A diamond sword could kill you in just a few hits!</li>
          </ul>
