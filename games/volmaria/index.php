              Fight along as a team/clan with your friends against others. Form alliances and become the strongest
              and most powerful faction on the server! For more information on factions, click the link above.
            </li>
            <li><b>PVP Classes:</b>
              Wear specific armor sets and get benefits. Full leather grants you the Archer class, full gold is the
              Rogue class, and finally, chainmail armor makes you an Orc. Each class grants you different PVP powers.
              Click the "PVP Classes" link at the top for the details on each class.
            </li>
            <li><b>PVP Tweaks:</b>
              Certain potions and enchantments are disabled on Volmaria. This encourages balanced PVP and using
              classes such as Rogue and Archer rather than full dimaond.
            </li>
            <li><b>Online Map:</b>
              View faction homes and find a place for your own using the <a href="http://volmaria.com:8123">server dynmap</a>.
