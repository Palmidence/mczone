            <p align="left"><strong>What should I do when I first login to the server, and what should I know?</strong></p>
            <ul>
              <li>Read the rules before anything else.</li>
              <li>Get at least ten fish.</li>
              <li>Use <span class="command">/wild</span> to escape spawn without being followed</li>
              <li>Find where you are on the dynmap, and find a location for your base.</li>
              <li>Mine down to Y=11 and start mining in the direction of your future base</li>
              <li>New players receive 60-minutes of PvP protection. This does not protect against mobs, falling damage, or other environment dangers.</li>
              <li><span class="command">/pvp time</span> allows you to check how much time you have before PvP protection runs is gone.</li>
              <li><span class="command">/pvp disable</span> will disable your PvP protection.</li>
              <li>PvP is enabled in all areas but the starting spawn.</li>
            </ul>
            <p align="left"><strong>What are the main parts of the map?</strong></p>
            <ul>
              <li>Spawn - The place where you sell iron/gold/diamond and are completely safe from PVP attacks (but not mobs). Teleport there by using the command, <span class="command">/spawn</span></li>
              <li>War Zone - It does not permit you to use buckets for lava reasons, and you shouldn't try to build a base in this area. You can not claim land in the war zone.</li>
              <li>Wilderness - The rest of the map. You can build anywhere and claim land for your faction, just be wary of other factions that may be nearby.</li>
              <li>A bird's eye view of the entire world can be viewed here: <a href="http://volmaria.com:8123">Dynmap</a></li>
            </ul>
            <p align="left"><strong>What is a faction? </strong></p>
            <p align="left">A faction is a group of people who</p>
            <ol>
              <li>Work together to pool resources and build a base.</li>
              <li>Make alliances and go to war with other factions.</li>
              <li>Build a base</li>
            </ol>
            <ul><li>Anything within a faction's land is protected, and individuals outside of a faction cannot open doors, break blocks, or open chests.</li></ul>
            <p align="left"><strong>How do I protect my faction land?</strong></p>
            <ul>
              <li>Make sure your base is completely sealed. An enemy can ender pearl in through a single open block.</li>
              <li>Do not build right up to the edge of your chunk.</li>
              <li>Do not place anything that can be activated by redstone near the edge of your chunk, especially doors</li>
              <li>Place an easily breakable block like sand inside of your doors to protect from zombies and door glitches</li>
              <li>Use <span class="command">/f sc</span> to see the edges of your chunk</li>
              <li>Use doors to block off each room of your base to limit the movement of an enemy that gets in.</li>
              <li>Build an ender portal in your base in an enclosed area</li>
            </ul>
            <p align="left">What are the advantages and disadvantages of a faction?</p>
            
            <p align="left"><strong>Advantages:</strong></p>
            <ul>
              <li>Players in a faction can claim land. This land will not allow other players to change blocks or access chests. This means your goods, so long as your faction has the appropriate power to maintain its land, are safe and you can build top side structures to your pleasure.</li>
              <li>Players in a faction are immune to dealing damage to each other. A small but powerful advantage in group PvP.</li>
              <li>So long as you are 100 blocks away from any other player, you can use the <span class="command">/f home</span> command, which will take you to your faction home. This allows player a fast travel benefit, but ensures that they cannot escape combat with the command.</li>
            </ul>

            <p align="left"><strong>Disadvantages:</strong></p>
            <ul>
              <li>Claimed land is visible on the dynamic map. Any player in the server can see where your land is. You will always have to be on your toes.</li>
              <li>If you or other faction members die, your power level may fall below the amount required to claim land. When this happens, your land is free to be destroyed and your chests to be accessed. So with protection comes the risk of losing all your work.</li>
              <li>There are no peaceful factions and no individual ownership in faction land. You can be infiltrated, and all faction members can change the claimed land blocks and access chests / use doors.</li>
            </ul>

            <p align="left"><strong>Can I join a faction immediately?</strong></p>
            <ul>
              <li>While it is possible that you can join a faction, most players will not let you join until you have established yourself (meaning you have some diamonds and can contribute to the sustainability of the group).</li>
              <li>Instead of spamming the in-game chat asking to join a faction, try to work on establishing your own supplies and materials first, and then ask if a faction is recruiting, or just create one on your own.</li>
            </ul>

            <p align="left"><strong>Do I have to create a faction?</strong></p>
            <ul>
              <li>No. You can remain factionless and build your home in the wilderness. The experience remains like any other hardcore death ban server. You can kill people in factions and roam the world as a lone wolf. If you decide to make a faction later on, you can.</li>
              <li>If you choose this option, your stuff and all of your belongings can be looted and griefed at any time, so beware.</li>
            </ul>

            <p align="left"><strong>How do I create a faction?</strong></p>
              <ul>
              <li>Type <span class="command">/f create [faction name]</span> Replacing the '[faction name]' with your desired name.</li>
              <li>For the rest of this FAQ, we will assume you named your faction NOOBIES</li>
            </ul>

            <p align="left"><strong>How do factions claim and declaim land?</strong></p>
            <ul>
              <li><strong></strong>Factions can only claim land once they have 2 or more members.</li>
              <li>Claiming land costs in-game money, called Coins.</li>
              <li>Land costs 100 coins for the first claim, and 50 more thereafter. So it is 100 for first, 150 for second, 200 for third, ect?</li>
            </ul>
            <strong>You can claim land in two ways.</strong>
            <ul>
              <li>Claim - type <span class="command">/f claim</span> - This claims the land you are standing on in a 16x16 area, from bedrock to block 256.</li>       
              <li>Autoclaim - Type <span class="command">/f autoclaim</span> - This will claim land that you walk on.</li>
            </ul>
            <p align="left"><strong>You can declaim land in 2 ways.</strong></p>

            <ul>
              <li>Type <span class="command">/f unclaim</span></li>
              <li>Type <span class="command">/f declaim</span></li>
              <li>Declaiming land returns to you 70% of the value of the land.</li>
            </ul>
            <strong>What important commands should I know once I make a faction?</strong>
            <ul>
              <li><span class="command">/f sethome</span> - This allows you to create an area inside your claimed land that you can teleport to at almost any time.</li>
              <li><span class="command">/f home</span> - This costs 30 coins, and takes 30 seconds to warm up. It will teleport you to your faction home.</li>
              <li>If you are within 100 blocks of an opposing player, you will not be able to use this command.</li>
              <li><span class="command">/f open</span> - Opens your faction and allows anyone to join your faction.</li>
              <li><span class="command">/f close</span> - Closes your faction and requires an invite in order to join.</li>
              <li><span class="command">/f invite [player name]</span> - Invites a player to your faction. - Invite may be shortened to <span class="command">/f inv [player name]</span>.</li>
              <li><span class="command">/f uninvite [player name]</span> - Remove a pending invitation.</li>
              <li>If your faction is closed invitations are required to join the faction.</li>
              <li><span class="command">/f desc [insert text here] </span>- Changes your faction's description. For instance, you could type <span class="command">/f desc We have no idea what we are doing.</span></li>
              <li><span class="command">/f officer [player name]</span> - This can only be done if you are the faction Administrator (creator). This gives or revokes officers rights to one of the members of the faction. It allows them to</li>
              <li> <span class="command">/f admin [player name]</span> - Hand over your admin rights to another member. Note that you are really handing it over. You will not be admin for the faction any longer. You will be turned into a officer, but the new admin could kick you any time.</li>
              <li><span class="command">/f kick [player name]</span> - Kick a player from the faction (will not work if player has negative power).</li>
              <li><span class="command">/f show</span> - This will show...
              <ul>
              <li>The power of your faction.</li>
              <li>The number of players in your faction.</li>
              <li>Who is online and offline.</li>
              <li>How many plots of land you own.</li>
            </ul>
            <ul>
              <li>Your faction's power.</li>
            </ul>
            </li>
              <li><span class="command">/f show [faction name]</span> - Shows the same information for another faction.</li>
              <li><span class="command">/f power</span> - Shows your power level.</li>
              <li><span class="command">/f leave</span> - Allows you to leave the faction at as long as your power is positive.</li>
            </ul>

            <p align="left"><strong>What else can I do with a faction?</strong></p>
            <ul>
              <li><span class="command">/f ally [faction name]</span> - Ally your faction with other factions. When you are allies, you cannot attack members of those factions, and they cannot attack you. * Note: Allies can build and destroy blocks on your land so choose allies carefully.</li>
              <li><span class="command">/f neutral [faction name]</span> - Places your faction in neutral status with another faction.</li>
              <li><span class="command">/f enemy [faction name] </span>- Places you in a hostile position with another faction.</li>
            </ul>

            <p align="left"><strong>How do factions claim my land, and how do I claim theirs?</strong></p>
            <ul>
              <li>A faction may claim your land if your power falls below zero, or if the number of plots you own is larger than the power of your faction.</li>
              <li>You may claim another faction's land if their power falls below zero, or if the number of plots they own is larger than the power of their faction.</li>
              <li>Claiming enemy faction land that is contested is 50 coins less than normal.</li>
            </ul>

            <p align="left"><strong>How does a faction's power increase or decrease?</strong></p>
            <ul>
              <li>Each individual in a faction can reach a maximum power of 10.</li>
              <li>The more individuals in a faction, the more power a faction will have (2 players = 20, 3 players = 30, etc).</li>
              <li>Your power (and faction power) decreases over time if you do not play consistently.</li>
              <li>Your power (and faction power) decreases by 20 each time you or someone else dies, assuming they have full power.</li>
              <li>You lose 20 power on death. This means you go from +10 (maximum) to -10 (minimum). This means when you die, you actually hurt your faction, as you cause a total power swing of 20, whereas each faction member can only be worth 10.</li>
            </ul>
            <p align="left"><strong>If I die, do I respawn immediately?</strong></p>

            <ul>
              <li>If you die, there is a three day death ban, so use your time wisely and be on the lookout for any attackers.</li>
            </ul>

            <p align="left"><strong>How do I get coins, and how do I view how many coins I have?</strong></p>
            <ul>
              <li>You receive 100 coins when you first login to the server.</li>
              <li><span class="command">/econ</span> - Allows you to view your coin total. - In order to get more coins, you must sell iron, gold, and diamonds at spawn.</li>
            </ul>
            <p align="left"><strong>Spawn Shop</strong></p>

            <ul>
              <li>Iron has a value of 5 coins per ingot (or 45 coins per iron block).</li>
              <li>Gold has a value of 10 coins per ingot (or 90 coins per gold block).</li>
              <li>Diamond has a value of 50 coins each (or 450 coins per diamond block)</li>
            </ul>
            <p align="left">To sell these ores, right click the proper box, and you will see your coin total increase each time.</p>
            <p align="left"><strong>How do I see how much money is in my faction, and what else can I do with the coins?</strong></p>
            <ul>
              <li><span class="command">/f money balance [faction]</span> - shows faction bank balance.</li>
              <li><span class="command">/f money deposit [amount]</span> - deposit money into faction bank.</li>
              <li><span class="command">/f money withdraw [amount]</span> - withdraw money from faction bank.</li>
              <li><span class="command">/f money ff [amount] NOOBIES [faction]</span> - transfer money from faction to faction.</li>
            </ul>

            
            <p align="left"><strong>Can I donate to the server, buy extra lives, and receive a reserved slot?</strong></p>
            <ul>
              <li>VIPs, Elites and Titans get a cool name color and a reserved slot on all MC Zone servers! They will also receive extra lives - 
                See a list of all the benefits at the <a href="/upgrade">upgrade page</a>.</li>
              <li>To buy extra lives without getting one of the subscriptions, type in your username at the top of this page and click "Buy".
            </ul>
