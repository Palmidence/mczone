          <h3>Orc</h3>

          Wear full chainmail armor and you will have the Orc class. Damage done with a diamond axe can do up to 3x the damage (much stronger than a diamond sword!)<br /><br />

          <b>Advantages:</b>
          <ul>
            <li>Brute force with the diamond axe can do a lot of damage in an instant.</li>
          </ul>
          <b>Disadvantages:</b>
          <ul>
            <li>Chainmail isn't the best armor, but it's decent.</li>
            <li>The inability to enchant the diamond axe.</li>
          </ul>
