          <h3>Archer</h3>
          Full leather grants you the benefits of an archer. Damage done with a bow is doubled when the target is under 20 blocks. If the distance
          is greater than 20, the damage is mulitplied by the distance divided by 10. For instance, if you damage a player from 35 blocks away, you would
          deal 3.5x damage to that player (35 &div; 10 = 3.5).
          <br /><br />
          <b>Advantages:</b>
          <ul>
            <li>Arrows can do significant damage when multiplied by 2x or greater. By adding other enchantments you can do even more damage!</li>
            <li>Do the most damage from long range attacks</li>
          </ul>
          <b>Disadvantages:</b>
          <ul>
            <li>Very low protection! A diamond sword could kill you in just a few hits!</li>
          </ul>

          <hr />



          <h3>Heavy</h3>
          Wear full diamond armor and get the basic benefits of using the strongest mineral in the game.
          <br /><br />
          <b>Advantages:</b>
          <ul>
            <li>Diamond armor!</li>
          </ul>
          <b>Disadvantages:</b>
          <ul>
            <li>No special effects like the other classes</li>
          </ul>
